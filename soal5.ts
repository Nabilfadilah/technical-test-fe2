// -----------------------------------------------
// Rumus: F = (C Ã— 9/5) + 32

function celsiusToFahrenheit(c: number): number {
  // rumus konversi celcius ke faranheit
  return (c * 9) / 5 + 32;
}

console.log("Soal 5:", celsiusToFahrenheit(30)); // 86

